#include "main.h"

/**
 * add - adds two numbers
 * @a: first number
 * @b: second number
 * Return: result of addition
 */
int add(int a, int b)
{
	int result;

	result = a + b;

	return (result);
}

/**
 * sub - subtracts two numbers
 * @a: first number
 * @b: second number
 * Return: result of subtraction
 */
int sub(int a, int b)
{
	int result;

	result = a - b;

	return (result);
}

/**
 * mul - multiplies two numbers
 * @a: first number
 * @b: second number
 * Return: result of multiplication
 */
int mul(int a, int b)
{
	int result;

	result = a * b;

	return (result);
}

/**
 * div - divides two numbers
 * @a: first number
 * @b: second number
 * Return: result of division
 */
int div(int a, int b)
{
	int result;

	result = a / b;

	return (result);
}

/**
 * mod - returns the remainder of dividing two numbers
 * @a: first number
 * @b: second number
 * Return: remainder
 */
int mod(int a, int b)
{
	int result;

	result = a % b;

	return (result);
}
